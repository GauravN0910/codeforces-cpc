#include<bits/stdc++.h>
using namespace std;

int main(){
    int t;
    cin>>t;
    while(t--){
        int n, k, q;
        cin>>n>>k>>q;
        vector<int> stopDistances(k+1);
        vector<int> stopTimes(k+1);

        for(int i=1;i<=k;i++){
            cin>>stopDistances[i];
        }

        for(int i=1;i<=k;i++){
            cin>>stopTimes[i];
        }

        while(q--){
            double d;
            cin>>d;
            
            int low = 1;
            int high = k;
            while(low<=high){
                int mid = low + (high-low)/2;
                if(stopDistances[mid]<d){
                    low = mid+1;
                }
                else{
                    high = mid-1;
                }
            }
            int ans = stopTimes[low-1] + (d-stopDistances[low-1]) * (stopTimes[low]-stopTimes[low-1])/(stopDistances[low]-stopDistances[low-1]);
            cout<<ans<<" ";
        }
        cout<<endl;
    }
}


